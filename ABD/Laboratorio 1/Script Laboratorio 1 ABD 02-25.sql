-- CREACION DE TABLAS
CREATE TABLE TREN (
    id INT PRIMARY KEY,
    serie VARCHAR(10),
    capacidad VARCHAR(20) NOT NULL
);

CREATE TABLE PAIS (
    id INT PRIMARY KEY,
    nombre VARCHAR(50)
);

CREATE TABLE PASAJERO (
    id INT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    identificacion VARCHAR(64) NOT NULL,
    fecha_nacimiento DATE NULL,
    email VARCHAR(50) NOT NULL,
    clave VARCHAR(32) NOT NULL,
    id_pais INT NOT NULL,
    puntos_viajero_frecuente INT NULL,
    FOREIGN KEY (id_pais) REFERENCES PAIS(id)
);

CREATE TABLE ESTACION (
    id INT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    id_pais INT NOT NULL,
    FOREIGN KEY (id_pais) REFERENCES PAIS(id)
);

CREATE TABLE VIAJE (
    id INT PRIMARY KEY,
    id_estacion_origen INT NOT NULL,
    id_estacion_destino INT NOT NULL,
    fecha_salida DATETIME,
    fecha_llegada DATETIME,
    id_tren INT NOT NULL,
    costo_base DECIMAL(18, 2),
    FOREIGN KEY (id_tren) REFERENCES TREN(id),
    FOREIGN KEY (id_estacion_origen) REFERENCES ESTACION(id),
    FOREIGN KEY (id_estacion_destino) REFERENCES ESTACION(id)
);

CREATE TABLE CLASE (
    id INT PRIMARY KEY,
    clase VARCHAR(15)
);

CREATE TABLE RESERVA (
    id INT PRIMARY KEY,
    costo DECIMAL(18, 2) NOT NULL,
    fecha_reserva DATE NOT NULL,
    id_pasajero INT NOT NULL,
    id_viaje INT NOT NULL,
    id_clase INT NOT NULL,
    FOREIGN KEY (id_pasajero) REFERENCES PASAJERO(id),
    FOREIGN KEY (id_viaje) REFERENCES VIAJE(id),
    FOREIGN KEY (id_clase) REFERENCES CLASE(id)
);

CREATE TABLE SERVICIO (
    id INT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    precio DECIMAL(18, 2) NOT NULL
);

CREATE TABLE EXTRA (
    id_servicio INT NOT NULL,
    id_reserva INT NOT NULL,
    cantidad INT NOT NULL,
    PRIMARY KEY (id_servicio, id_reserva),
    FOREIGN KEY (id_servicio) REFERENCES SERVICIO(id),
    FOREIGN KEY (id_reserva) REFERENCES RESERVA(id)
);

